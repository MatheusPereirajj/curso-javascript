/*
OPERADORES DE COMPARAÇÃO
>   maior que
>=  maior ou igual a 
<   menor que 
<=  menor ou igual a
=== igualdade estrita (valor e tipo)
!== diferente estrito (valor e tipo)
*/ 

//comparação 1
let a = 10;
let b = 11;
let comp = a !== b;
